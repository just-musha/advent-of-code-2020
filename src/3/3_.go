package main

import (
	"fmt"
	"strings"
)

type Slope struct {
	i int // index in row
	r int // row
}

var slopes = []Slope{
	{i: 1, r: 1},
	{i: 3, r: 1},
	{i: 5, r: 1},
	{i: 7, r: 1},
	{i: 1, r: 2},
}

func countTrees(s Slope, rows []string) int {

	maxi := len(rows[0])
	maxr := len(rows)

	treeCnt := 0
	i, r := 0, 0

	for r < maxr {

		// check if tree
		if rows[r][i] == '#' {
			treeCnt++
		}

		// move
		i = (i + s.i) % maxi
		r = r + s.r
	}
	return treeCnt
}

